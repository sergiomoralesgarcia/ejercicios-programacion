/**
 * Muestra los números del 320 al 160, contando de 20 en 20 hacia atrás utilizando
 * un bucle while.
 *
 * @author Sergio Morales García
 */
public class Ejercicio05 {
    public static void main(String[] args) {
      int i = 340;
      
      while (i > 160) {
        System.out.println(i -= 20);
      }
    }
  }